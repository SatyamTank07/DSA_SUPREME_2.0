using namespace std;

int main()
{
    // Nested If Condition
    int a = 5, b = 10, c = 15;

    if (a > 5)
    {
        if (c > 10)
        {
            cout << "Sainik";
        }
    }
    else
    {
        if (b = 10)
        {
            if (c >= 10)
            {
                cout << "Shipahi";
            }
        }
    }

    return 0;
}
